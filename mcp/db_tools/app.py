@app.post("/<slug>/refresh/repo")
def refresh_repo(slug: str):
    repo_root, _, _ = _paths(slug)
    if repo_root is None:
        return jsonify({"error": f"Repo '{slug}' not found"}), 404
    try:
        result = subprocess.run(
            ["graphify", "update", ".", "--force"],
            cwd=repo_root, capture_output=True, text=True,
            timeout=300, check=True,
        )
    except FileNotFoundError:
        return jsonify({"error": "graphify not found — install with: pip install graphifyy"}), 500
    except subprocess.TimeoutExpired:
        return jsonify({"error": "Code graph refresh timed out"}), 504
    except subprocess.CalledProcessError as exc:
        print(f"refresh_repo: graphify failed for {slug!r} (exit {exc.returncode}): {exc.stderr}", file=sys.stderr)
        return jsonify({"error": "Code graph refresh failed — see server logs"}), 500
    return jsonify({
        "status": "refreshed",
        "graph_type": "code",
        "output": result.stdout.strip(),
        "graph_url": url_for("code_graph", slug=slug),
    })


@app.post("/<slug>/refresh/db")
def refresh_db(slug: str):
    repo_root, _, _ = _paths(slug)
    if repo_root is None:
        return jsonify({"error": f"Repo '{slug}' not found"}), 404

    env_vars = dotenv_values(repo_root / ".env")
    db_conn_str = env_vars.get("DB_CONNECTION_STRING") or os.getenv("DB_CONNECTION_STRING")
    if not db_conn_str:
        return jsonify({"error": "DB_CONNECTION_STRING is not set"}), 500
    if not BUILD_SCRIPT.exists() or not VISUALIZE_SCRIPT.exists():
        return jsonify({"error": "build_db_graph.py or build_graph_html.py is missing"}), 500

    env = {**os.environ, "DB_CONNECTION_STRING": db_conn_str}
    try:
        subprocess.run(
            [sys.executable, str(BUILD_SCRIPT)],
            cwd=repo_root, capture_output=True, text=True,
            timeout=120, check=True, env=env,
        )
        subprocess.run(
            [sys.executable, str(VISUALIZE_SCRIPT)],
            cwd=repo_root, capture_output=True, text=True,
            timeout=120, check=True, env=env,
        )
    except subprocess.TimeoutExpired:
        return jsonify({"error": "Database graph refresh timed out"}), 504
    except subprocess.CalledProcessError as exc:
        # Do NOT log or return the subprocess output: build_db_graph.py runs with
        # DB_CONNECTION_STRING in its env and its errors may echo that credential.
        print(f"refresh_db: build failed for {slug!r} (exit {exc.returncode}); "
              f"output withheld (may contain DB credentials)", file=sys.stderr)
        return jsonify({"error": "Database graph refresh failed — re-run the build manually to see details"}), 500

    # Do NOT return build/visualize stdout: those subprocesses run with
    # DB_CONNECTION_STRING in their env and could echo it. The UI only needs the
    # status and the graph URL.
    print(f"refresh_db: rebuilt database graph for {slug!r}", file=sys.stderr)
    return jsonify({
        "status": "refreshed",
        "graph_type": "database",
        "graph_url": url_for("db_graph", slug=slug),
    })


