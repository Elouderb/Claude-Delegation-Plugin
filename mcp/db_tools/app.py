@app.before_request
def _require_xrw_for_writes():
    """CSRF guard for state-changing requests.

    The refresh endpoints spawn subprocesses, so a forged cross-site POST must
    not be able to trigger them. Requiring a non-simple header
    (``X-Requested-With``) forces a CORS preflight for any cross-origin request,
    which this server never answers permissively — so only same-origin fetch()
    from our own pages (which set the header) is accepted. No authentication is
    added: on the trusted-LAN deployment the bind intentionally stays 0.0.0.0.
    """
    if request.method in ("POST", "PUT", "PATCH", "DELETE"):
        if request.headers.get("X-Requested-With") != "XMLHttpRequest":
            return jsonify({"error": "Missing X-Requested-With header"}), 403


